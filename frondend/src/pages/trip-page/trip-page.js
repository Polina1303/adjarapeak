function isWeekend(date) {
  const day = date.getDay();
  return day === 0 || day === 6;
}

function parseSeasonEnd(seasonEndStr) {
  const parts = seasonEndStr.split(" ");
  const dayStr = parts[0];
  const monthStr = parts[1]?.toLowerCase();
  const yearStr = parts[2] || new Date().getFullYear().toString();

  const day = parseInt(dayStr, 10);
  const month = monthMap[monthStr];
  const year = parseInt(yearStr, 10);

  if (isNaN(day) || month === undefined || isNaN(year)) {
    console.warn("–ù–µ —É–¥–∞–ª–æ—Å—å —Ä–∞—Å–ø–∞—Ä—Å–∏—Ç—å –¥–∞—Ç—É –æ–∫–æ–Ω—á–∞–Ω–∏—è —Å–µ–∑–æ–Ω–∞:", seasonEndStr);
    return null;
  }

  const seasonEndDate = new Date(year, month, day);
  seasonEndDate.setHours(23, 59, 59, 999);

  return seasonEndDate;
}
function parseEventDate(dateStr) {
  const trimmed = dateStr.trim();

  if (
    trimmed.includes("–í –ª—é–±–æ–π –∏–∑ –¥–Ω–µ–π") ||
    trimmed.includes("–ø–æ –∑–∞–ø—Ä–æ—Å—É") ||
    trimmed === "everyday" ||
    trimmed === "weekend"
  ) {
    return null;
  }

  const clean = trimmed.replace(/\s*\([^)]*\)$/, "").trim();

  const parts = clean.split(/\s+/);

  const dayStr = parts[0];
  const monthStr = parts[1]?.toLowerCase();

  const day = parseInt(dayStr.replace(/\D/g, ""), 10);
  const month = monthMap[monthStr];

  if (isNaN(day) || month === undefined) {
    console.warn(
      "–ù–µ —É–¥–∞–ª–æ—Å—å —Ä–∞—Å–ø–∞—Ä—Å–∏—Ç—å –¥–∞—Ç—É:",
      dateStr,
      "day:",
      dayStr,
      "month:",
      monthStr
    );
    return null;
  }

  const year = new Date().getFullYear();
  const eventDate = new Date(year, month, day);
  eventDate.setHours(0, 0, 0, 0);

  const now = new Date();
  if (eventDate < now) {
    return null;
  }

  return eventDate;
}

